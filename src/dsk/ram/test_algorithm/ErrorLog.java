package dsk.ram.test_algorithm;



public class ErrorLog {
	private int x;
	private int y;
	private boolean value;
	
	public ErrorLog(int x, int y, boolean value) {
		super();
		this.x = x;
		this.y = y;
		this.value = value;
	}

	public int getX() {
		return x;
	}

	public int getY() {
		return y;
	}

	public boolean isValue() {
		return value;
	}
}
